exports.createTour = (req, res) => {
  const newID = tours[tours.length - 1].id + 1;
  const newTour = {
    id: newID,
    // eslint-disable-next-line node/no-unsupported-features/es-syntax
    ...req.body,
  };

  tours.push(newTour);

  fs.writeFile(
    `${__dirname}/dev-data/data/tours-simple.json`,
    JSON.stringify(tours),
    () => {
      res.status(201).json({
        status: 'success',
        data: {
          tour: newTour,
        },
      });
    }
  );
};
